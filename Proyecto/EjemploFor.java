package Proyecto;

public class EjemploFor {


        public static void main(String[] args) {
            // Declarar una variable entera para la suma
            int suma = 0;

            // Crear un ciclo for que se ejecute 10 veces
            for (int i = 1; i <= 10; i++) {
                // Agregar el valor de i a la suma
                suma += i;
            }

            // Imprimir el resultado de la suma
            System.out.println("La suma de los nÃºmeros del 1 al 10 es: " + suma);
        }

    }


